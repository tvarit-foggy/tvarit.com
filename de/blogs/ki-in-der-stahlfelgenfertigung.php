            </p>
            <p class="text-medium">
              Angewandt auf die Praxis bedeutet das, dass Prozessempfehlungen für optimale Qualität datenbasiert getroffen werden können und so der Aufwand für manuelle Auswertungen minimiert wird. Dadurch steigt im Endeffekt die OEE und die Ausschussrate sinkt. Außerdem werden Fehler durch die Echtzeit-Qualitätsüberwachung schon bei Entstehung identifiziert und wenn möglich angepasst. Sollte eine Ausbesserung des Fehlers nicht möglich sein, so wird durch ein gezieltes ausweisen der Bauteile vermieden, dass nachfolgende Prozessschritte wie das Entgraten bei niO Teilen durchgeführt werden. Dadurch wird Maschinenkapazität frei und fehlerhafte Bauteile werden erst gar nicht weiterverarbeitet.

            </p>
            <br>
            <h5 class="red">Welche Vorteile bringt das Ganze – und wie werden diese erreicht?</h5>

            <p class="text-medium"> Ich habe es eben bereits kurz vorweggenommen, möchte aber nun nochmal genauer auf die Benefits von industrieller KI in der Felgenfertigung eingehen. Irgendeinen Grund muss es ja schließlich haben, dass deutlich über die Hälfte der Industrieunternehmen KI in den nächsten Jahren eine entscheidende Bedeutung zuweist. In der Felgenfertigung wäre das zum einen ganz salopp die Erhöhung der OEE, also der aus produktionstechnisch/betriebswirtschaftlich betrachteten Sicht wichtigsten Kennziffer. Erreicht wird das auf Basis von mehreren KI-Techniken, im Fokus steht allerdings ein mit Hilfe von Produktionsdaten trainiertes Modell mit welchem während des Fertigungsprozesses die Qualität der Bauteile bewertet werden kann. Dadurch können Bottle-Neck Prozesse wie ein Bauteilröntgen in der End of Line Kontrolle für bereits als Schlechtteil klassifizierte Teile übersprungen werden und so Kapazität geschaffen werden. Durch die erweiterten
            Analysemöglichkeiten der Prozessdaten mit Hilfe von digitalen Dashboards lassen sich zudem Schwachstellen und Optimierungspotenziale entlang der Felgenfertigung identifizieren. Auf diese Weise können in Folge der bereits erwähnten OEE-Erhöhung sowohl Stromkosten als auch Liefer-und Produktionszeiten gesenkt werden.</p>
            <br>
            <h5 class="red">Volle Digitalisierung mit einem digitalen Zwilling</h5>
            <p class="text-medium">Damit erst gar keine Schlechtteile produziert werden können, hilft das trainierte Modell mit verschiedenen Parameter-Anpassungen, um den Gussprozess besser verstehen zu können. Auf diese Weise können während des Betriebs laufend die optimalen Parameter in präskriptiven Rezepten vorgeschlagen werden. Mit Hilfe eines interaktiven Dashboards können zusätzlich verschiedene Produktionsparameter rein virtuell in einer Art digitalen Zwilling manipuliert werden, um den Effekt der jeweiligen Parameterveränderung auf die Produktion besser verstehen zu können. Dabei kann beispielsweise ein Szenario simuliert werden, in welchem die Temperatur der Wärmebehandlung verändert wird und geschaut wird, wie sich das auf die Produktqualität und den Ausschuss auswirkt. Datengrundlage für diese Prognosen bilden auch hier die bereits im Trainingsdatensatz aufgenommenen Prozessparameter.</p>
            <p class="text-medium">Zusammenfassend lässt sich sagen, dass Dashboards für die Anzeige von Prozessdaten sehr nützlich sein können, wenn sie den logisch durchdacht und auf die Bedürfnisse der Felgenfertigung angepasst sind. Guss- und die Folgeprozesse eignen sich zudem sehr gut dafür, überwacht und als digitaler Zwilling angezeigt zu werden.</p>


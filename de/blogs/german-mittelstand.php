            </p>
            <p class="text-medium">In der Industrie-Praxis handelt es sich heute vorwiegend um anwendungsspezifische Lösungen für dedizierte Problemstellungen – beispielsweise, wenn Qualitätsabweichungen in einem gewissen Prozess mit Hilfe von maschinellem Lernen erkannt werden. </p>
            <p class="text-medium">Des Weiteren kann KI historische Daten verwenden um mehrdimensionale, komplexe Zusammenhänge und Trends zu identifizieren. Der Zeitpunkt für den Austausch von Komponenten kann somit valide vorausgesagt werden, bevor dieser ungeplant ansteht, Stillstandzeiten verursacht und die OEE belastet.</p>
            <h5 class="red">Was sagt der Mittelstand dazu? Ist KI für KMUs relevant?</h5>
            <p class="text-medium">Sehen wir, was der Mittelstand selbst dazu sagt: Laut einer Studie von Price Waterhouse Coopers sehen 70 Prozent der befragten Experten aus KMUs die Gefahr, dass der deutsche Mittelstand im Zuge der internationalen Entwicklungen rund um Künstliche Intelligenz abgehängt wird, obwohl – und das ist das überraschende – 77 Prozent der Befragten KI nicht für einen Hype, sondern bedeutend für die Zukunft des deutschen Mittelstands halten. Keine guten Aussichten also?</p>
            <p class="text-medium">Nicht ohne Grund hat die Bundesregierung im November 2018 die „Nationale Strategie für Künstliche Intelligenz“ verabschiedet. Mit Förderprogrammen, Initiativen und Kooperationen versucht man Deutschland zu einem führenden Standort für Künstliche Intelligenz zu machen. Die Strategie lässt sich dabei in zwölf Handlungsfelder von Forschung und Innovationswettbewerben über die Gestaltung des Strukturwandels in der Arbeitswelt und Stärkung der Ausbildung bis hin zur Erleichterung der Datennutzung und Festlegung von DIN-Norm Standards unterteilen. </p>
            <p class="text-medium">Doch warum bedarf es überhaupt einer staatlichen Strategie bei einer Technologie, die auf den ersten Blick ja durch betriebswirtschaftliche und technologische Vorteile so attraktiv für Unternehmer sein sollte, dass durch den freien Markt eine ausreichende Innovation stattfinden sollte.</p>
            <p class="text-medium">Um zu verstehen, warum Marktkräfte hierbei nicht allein ausreichen, müssen wir uns den Status Quo in deutschen Mittelstands Unternehmen anschauen. Beispielsweise sehen 100% der befragten Mittelständler fehlendes Know-how oder fehlende Fachkräfte als ein starkes oder gar sehr starkes Hemmnis bei der Nutzung von KI im eigenen Unternehmen. Auch Datensicherheitsbedenken und eine nicht ausreichende Datenbasis sowie nicht ausreichende Digitalisierung im restlichen Unternehmen sehen die Mittelständler als Problem an.</p>
            <h5 class="red">Auf Partnerschaften vertrauen</h5>
            <p class="text-medium">Politische Rahmenbedingungen sind mit der KI-Initiative der Bundesregierung abgesteckt. Unternehmerischen Mut haben mittelständische Unternehmen seit je her bewiesen. Es gibt also keinen Grund zu warten und das enorme Potenzial von KI zu heben. Denn genau das sagen die Analysten von PWC voraus: Ein Wachstum von 11,3 Prozent beim deutschen BIP bis 2030 aufgrund KI-basierter Innovationen. </p>
            <p class="text-medium">Um dieses aber nun auch zu realisieren, muss dem deutschen Mittelstand geholfen werden, Hürden wie fehlendes Know-how oder Fachkräfte zu überkommen. Mit unserer Expertise können wir deutschen Mittelständlern auf der gesamten Reise hin zur Digitalisierung begleiten. Es bedarf hierbei eines Teams von Experten, welches sich ebenso gut mit den KI-Algorithmen auskennt, wie mit den Prozessen und damit verbundenen Problemen im Unternehmen selbst. Nur durch eine enge Zusammenarbeit ist es möglich, langfristig skalierbare Lösungen und den damit einhergehenden Mehrwert zu generieren. </p>
            <p class="text-medium">So gelingt es, KI als ein hilfreiches Werkzeug zu nutzen, welches datenbasiert neue Erkenntnisse schafft, Prozesse optimiert und bessere operative Geschäftsentscheidungen ermöglicht.</p>


            <h4 class="red fw-700">Prozessbeschreibung</h4>
            <h3>Der Prozess erfolgt in drei Schritten:</h3>


            <br>

            <h5 class="red">Schritt 1:</h5>
            <p class="text-p">
                Positionieren des Ausgangsmaterials: Die geformten Metallbleche werden durch eine automatische
                Vorrichtung in die entsprechende Position gebracht, sodass die Blechkanten miteinander verschweißt
                werden können.
            </p>
            <br>

            <h5 class="red">Schritt 2:</h5>
            <p class="text-p">
                Ausführen des Schweißvorgangs: Die beiden Blechkanten werden zwischen den Elektroden der Schweißanlage
                eingespannt und die zu verscheißenden Kanten aneinander gepresst. Unter Anbringung des Schweißstroms
                werden die Kanten innerhalb von zwei bis fünf Sekunden miteinander verschweißt
            </p>
            <br>

            <h5 class="red">Schritt 3:</h5>
            <p class="text-p">
                Nachdem der Schweißwulst entfernt wurde, wird das Material zur Weiterverarbeitung befördert.
            </p>
            <br>
            <h4 class="red fw-700">Qualitätskontrolle:</h4>
            <p class="text-p">
                Die Qualitätskontrolle erfolgt durch automatisierte Röntgentests sowie einer manuellen metallurgischen
                Inspektion des Ausgangsmaterials als auch in Form von 8D-Reports im Falle von Kundenreklamationen,
            </p>
            <br>
            <h4 class="red fw-700">Wartung der Maschine:</h4>
            <p class="text-p">
                Die Schweißelektroden (Kupferelektroden) werden in regelmäßigen Abständen ausgetauscht. (einige der
                Elektroden 2-mal täglich, andere monatlich).
            </p>
            <br>
            <h4 class="red fw-700">Auftretende Probleme und Herausforderungen:</h4>
            <h5 class="red">Qualitätsmängel von 5%</h5>
            <p class="text-p">
                Bauteile mit fehlerhaften Schweißnähten werden entweder direkt verschrottet oder müssen aufwändig
                nachbearbeitet werden. Im Falle der Nacharbeit muss die Schweißnaht zunächst geöffnet, das Bauteil für
                den Schweißprozess erneut vorbereitet und die Schweißnaht erneut gesetzt werden, wodurch ein
                signifikanter Mehraufwand entsteht.
            </p>
            <br>
            <h5 class="red">Hoher Elektroden-Verschleiß im Schweißprozess.</h5>
            <p class="text-p">
                Dabei wirkt sich der Zustand der Anoden stark auf die Qualität der Schweißnaht und somit auf die Menge
                der entstehenden Qualitätsmängel aus.
            </p>

